void EpdSolver::solver(Files &files){

  int nStep = intTemp->get_nStep();
  double *uCell = mesh->get_cells().get_u();

  // ...
  times.init_timer();
  mesh->writeGeomNode(files.get_fileOutNode());
  mesh->writeGeomCell(files.get_fileOutCell());
  times.updateResTimer();
  // ............................................................................

  //
  mesh->nodalInterpol();
  // ............................................................................

  // ...
  times.init_timer();
  mesh->resNode(files.get_fileOutNode(), *intTemp);
  mesh->resCell(files.get_fileOutCell(), *intTemp);
  times.updateResTimer();
  // ............................................................................

  cout << "Running ..." << endl;

  for (int j = 0; j < nStep; j++) {

    // ...
    intTemp->updateTime();
    // ..........................................................................

    // ...
    times.init_timer();
    cellLoop->montaSistema();
    times.updateSistTimer();
    // ..........................................................................

    //... solver
    times.init_timer();
    uCell = solverEq->solver(uCell);
    times.updateSolverTimer();
    //...........................................................................

    // ... 
    mesh->nodalInterpol();
    //...........................................................................

    // ...
    times.init_timer();
    mesh->resNode(files.get_fileOutNode(), *intTemp);
    mesh->resCell(files.get_fileOutCell(), *intTemp);
    times.updateResTimer();
    // ..........................................................................   

  }
// ..............................................................................

}
